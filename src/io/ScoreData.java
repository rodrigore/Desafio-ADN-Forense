package io;

public class ScoreData {

	String baseDNA;
	String tupleScore;
	public ScoreData(String baseDNA, String tupleScore) {
		this.baseDNA = baseDNA;
		this.tupleScore = tupleScore;
	}
	
	public String getBaseDNA() {
		return baseDNA;
	}

	public String getTupleScore() {
		return tupleScore;
	}
	
	
}
